/**
 * Content block events (text, images, etc.)
 */
export type ContentStartEvent = {
  type: "content_start";
  blockType: BlockType;
  blockIndex: number;
} & StreamEventBase;

export type ContentDeltaEvent = {
  type: "content_delta";
  blockType: BlockType;
  blockIndex: number;
  delta: string;
} & StreamEventBase;

export type ContentEndEvent = {
  type: "content_end";
  blockType: BlockType;
  blockIndex: number;
} & StreamEventBase;

export type ContentEvent = {
  type: "content";
  blockIndex: number;
  content: ContentBlock;
  startedAt: string;
  completedAt: string;
} & StreamEventBase;

/**
 * Reasoning/thinking events (separate for easy filtering)
 */
export type ReasoningStartEvent = {
  type: "reasoning_start";
  blockIndex: number;
} & StreamEventBase;

export type ReasoningDeltaEvent = {
  type: "reasoning_delta";
  blockIndex: number;
  delta: string;
} & StreamEventBase;

export type ReasoningEndEvent = {
  type: "reasoning_end";
  blockIndex: number;
} & StreamEventBase;

export type ReasoningEvent = {
  type: "reasoning";
  blockIndex: number;
  reasoning: string;
  startedAt: string;
  completedAt: string;
} & StreamEventBase;

/**
 * Message lifecycle events
 */
export type MessageStartEvent = {
  type: "message_start";
  role: "assistant";
  model?: string;
} & StreamEventBase;

export type MessageEndEvent = {
  type: "message_end";
  stopReason: StopReason;
  usage?: UsageStats;
} & StreamEventBase;

export type MessageEvent = {
  type: "message";
  message: Message;
  stopReason: StopReason;
  usage?: UsageStats;
  model?: string;
  /** Raw provider response (e.g., AI SDK generateText result, OpenAI ChatCompletion) */
  raw?: unknown;
  startedAt: string;
  completedAt: string;
} & StreamEventBase;

/**
 * Tool call events (model requesting tool execution)
 */
export type ToolCallStartEvent = {
  type: "tool_call_start";
  callId: string;
  name: string;
  blockIndex: number;
} & StreamEventBase;

export type ToolCallDeltaEvent = {
  type: "tool_call_delta";
  callId: string;
  blockIndex: number;
  delta: string;
} & StreamEventBase;

export type ToolCallEndEvent = {
  type: "tool_call_end";
  callId: string;
  blockIndex: number;
} & StreamEventBase;

export type ToolCallEvent = {
  type: "tool_call";
  callId: string;
  blockIndex: number;
  name: string;
  input: Record<string, unknown>;
  startedAt: string;
  completedAt: string;
} & StreamEventBase;

/**
 * Error event
 */
export type StreamErrorEvent = {
  type: "error";
  error: {
    message: string;
    code?: string;
  };
} & StreamEventBase;

/**
 * ModelStreamEvent - All model output events.
 *
 * Discriminated union of all events that come from model streaming.
 */
export type ModelStreamEvent =
  // Content blocks
  | ContentStartEvent
  | ContentDeltaEvent
  | ContentEndEvent
  | ContentEvent
  // Reasoning/thinking
  | ReasoningStartEvent
  | ReasoningDeltaEvent
  | ReasoningEndEvent
  | ReasoningEvent
  // Message lifecycle
  | MessageStartEvent
  | MessageEndEvent
  | MessageEvent
  // Tool calls
  | ToolCallStartEvent
  | ToolCallDeltaEvent
  | ToolCallEndEvent
  | ToolCallEvent
  // Errors
  | StreamErrorEvent;

